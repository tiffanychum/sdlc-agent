# ── Schema conversion ─────────────────────────────────────────────

JSON_TYPE_MAP = {
    "string": str,
    "integer": int,
    "number": float,
    "boolean": bool,
    "array": list,
    "object": dict,
}


def _resolve_type(prop_schema: dict) -> type:
    """Resolve a JSON Schema property definition to a Python type.

    Handles both simple {"type": "string"} and anyOf/Union schemas
    generated by FastMCP for Optional parameters (e.g. int | None).
    """
    type_str = prop_schema.get("type")
    if type_str:
        return JSON_TYPE_MAP.get(type_str, str)

    # Handle anyOf for Union types (e.g. int | None → anyOf: [{type: integer}, {type: null}])
    for sub in prop_schema.get("anyOf", []):
        if sub.get("type") != "null":
            return JSON_TYPE_MAP.get(sub.get("type", "string"), str)

    return str


def _schema_to_pydantic(tool_name: str, schema: dict) -> type[BaseModel]:
    """Convert a JSON Schema from a FastMCP tool into a Pydantic model.

    Optional parameters are represented with concrete types + sentinel defaults
    rather than Optional[X] so that Pydantic v2 does NOT emit anyOf in the
    serialised JSON Schema. The Poe API returns HTTP 500 for schemas that
    contain anyOf constructs.
    """
    properties = schema.get("properties", {})
    required = set(schema.get("required", []))
    fields: dict[str, Any] = {}

    for prop_name, prop_schema in properties.items():
        py_type = _resolve_type(prop_schema)
        description = prop_schema.get("description", "")
        default = prop_schema.get("default")

        if prop_name in required:
            fields[prop_name] = (py_type, Field(description=description))
        elif default is not None:
            # Use the concrete type with its default — avoids Optional/anyOf in JSON Schema
            fields[prop_name] = (py_type, Field(default=default, description=description))
        else:
            # No default in schema; use the sentinel value appropriate for the type
            _sentinel: Any = "" if py_type is str else (0 if py_type is int else (0.0 if py_type is float else (False if py_type is bool else None)))
            fields[prop_name] = (py_type, Field(default=_sentinel, description=description))

    model_name = "".join(part.capitalize() for part in tool_name.split("_")) + "Args"
    return create_model(model_name, **fields)


# ── FastMCP → LangChain bridge ────────────────────────────────────

def _make_langchain_tool(tool, mcp_server) -> StructuredTool:
    """Create a LangChain StructuredTool from a FastMCP FunctionTool."""
    schema = tool.parameters or {"type": "object", "properties": {}}
    args_model = _schema_to_pydantic(tool.name, schema)
    tool_name = tool.name
    tool_description = tool.description or ""

    # Names of required parameters so we never strip them even if falsy
    _required = set(schema.get("required", []))

    async def _invoke(**kwargs) -> str:
        # Strip optional params that have their sentinel/None value so FastMCP
        # falls back to the function's own default.
        cleaned = {
            k: v for k, v in kwargs.items()
            if k in _required or v is not None
        }
        result = await mcp_server.call_tool(tool_name, cleaned)
        return result.content[0].text if result.content else "No result"

    return StructuredTool.from_function(
        coroutine=_invoke,
        name=tool_name,
        description=tool_description,
        args_schema=args_model,
        func=lambda **kw: None,
    )


async def _get_tools_for(mcp_server) -> list[StructuredTool]:
    """Discover all tools from a FastMCP server and wrap them for LangChain."""
    tools = await mcp_server.list_tools()
    return [_make_langchain_tool(t, mcp_server) for t in tools]


