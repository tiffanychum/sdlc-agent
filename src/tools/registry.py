def get_rag_tools() -> list[StructuredTool]:
    """Return RAG search tools: general rag_search + domain-specific perf_search."""

    # ── Shared helper ──────────────────────────────────────────────────────────

    async def _run_rag_query(
        query: str,
        config_id: Optional[str] = None,
        use_compression: bool = False,
    ) -> str:
        """Resolve a RAG config, run the pipeline, and format citations."""
        from src.db.database import get_session
        from src.db.models import RagConfig as RagConfigModel
        from src.rag.pipeline import RAGConfig, get_pipeline

        session = get_session()
        try:
            if config_id:
                cfg_row = session.query(RagConfigModel).filter_by(id=config_id, is_active=True).first()
            else:
                cfg_row = session.query(RagConfigModel).filter_by(is_active=True).first()
        finally:
            session.close()

        if not cfg_row:
            return "No active RAG knowledge base found. Please configure and ingest documents first."

        cfg = RAGConfig(
            config_id=cfg_row.id,
            name=cfg_row.name,
            embedding_model=cfg_row.embedding_model,
            vector_store=cfg_row.vector_store,
            llm_model=cfg_row.llm_model,
            chunk_size=cfg_row.chunk_size,
            chunk_overlap=cfg_row.chunk_overlap,
            chunk_strategy=cfg_row.chunk_strategy,
            retrieval_strategy=cfg_row.retrieval_strategy,
            top_k=cfg_row.top_k,
            mmr_lambda=cfg_row.mmr_lambda or 0.5,
            multi_query_n=cfg_row.multi_query_n or 3,
            system_prompt=cfg_row.system_prompt,
        )
        pipeline = get_pipeline(cfg)
        if pipeline.chunk_count() == 0:
            return f"RAG knowledge base '{cfg_row.name}' has no ingested documents yet."

        response = await pipeline.query(query, use_compression=use_compression)
        lines = [f"**Knowledge Base Answer** (from '{cfg_row.name}'):\n", response.answer, ""]
        if response.citations:
            lines.append("**Sources:**")
            seen = set()
            for i, c in enumerate(response.citations):
                key = f"{c.source}:{c.chunk_index}"
                if key not in seen:
                    seen.add(key)
                    page_info = f", p.{c.page}" if c.page else ""
                    lines.append(f"[{i + 1}] {c.source}{page_info} (relevance: {c.score:.2f})")
        return "\n".join(lines)

    # ── Tool 1: general rag_search ─────────────────────────────────────────────

    class RagSearchArgs(BaseModel):
        query: str = Field(description="The search query to find relevant information from the knowledge base.")
        config_id: Optional[str] = Field(
            default=None,
            description="RAG config ID to query. If omitted, queries the first active config.",
        )

    async def _rag_search(query: str, config_id: Optional[str] = None) -> str:
        """Query the RAG knowledge base and return cited context."""
        try:
            return await _run_rag_query(query, config_id=config_id, use_compression=False)
        except Exception as e:
            return f"RAG search error: {e}"

    # ── Tool 2: perf_search — always targets Performance Knowledge Base ─────────

    class PerfSearchArgs(BaseModel):
        query: str = Field(
            description=(
                "Performance analysis question. E.g.: "
                "'Why did P99 latency spike in SEA?', "
                "'How to attribute a frame-rate drop?', "
                "'What metrics matter for live stream quality?', "
                "'How to detect A/B experiment novelty effect?'"
            )
        )
        use_compression: bool = Field(
            default=True,
            description=(
                "Apply LLM-based contextual compression to filter out irrelevant "
                "sentences from retrieved chunks before generating the answer. "
                "Reduces hallucination; adds ~1s latency. Default: True."
            ),
        )

    async def _perf_search(query: str, use_compression: bool = True) -> str:
        """
        Query the Performance Analysis Knowledge Base — playbooks, metric taxonomies,
        A/B experiment templates, incident post-mortems, and code review patterns.

        Always use this BEFORE web_search when investigating:
        - Live stream performance anomalies (latency, frame rate, bitrate)
        - A/B experiment methodology or statistical analysis
        - Performance code review patterns (shift-left)
        - Historical incident attribution patterns
        """
        try:
            from src.rag.performance_kb import PERF_KB_CONFIG_ID, seed_performance_kb
            from src.db.database import get_session
            from src.db.models import RagConfig as RagConfigModel
            from src.rag.pipeline import RAGConfig, get_pipeline

            # Auto-seed the KB if it has no documents yet
            session = get_session()
            try:
                cfg_row = session.query(RagConfigModel).filter_by(
                    id=PERF_KB_CONFIG_ID, is_active=True
                ).first()
            finally:
                session.close()

            if cfg_row:
                cfg = RAGConfig(
                    config_id=cfg_row.id,
                    name=cfg_row.name,
                    embedding_model=cfg_row.embedding_model,
                    vector_store=cfg_row.vector_store,
                    llm_model=cfg_row.llm_model,
                    chunk_size=cfg_row.chunk_size,
                    chunk_overlap=cfg_row.chunk_overlap,
                    chunk_strategy=cfg_row.chunk_strategy,
                    retrieval_strategy=cfg_row.retrieval_strategy,
                    top_k=cfg_row.top_k,
                    mmr_lambda=cfg_row.mmr_lambda or 0.5,
                    multi_query_n=cfg_row.multi_query_n or 3,
                    system_prompt=cfg_row.system_prompt,
                )
                pipeline = get_pipeline(cfg)
                if pipeline.chunk_count() == 0:
                    await seed_performance_kb()
                    import asyncio
                    from src.rag.pipeline import evict_pipeline
                    evict_pipeline(PERF_KB_CONFIG_ID)
            else:
                await seed_performance_kb()

            return await _run_rag_query(
                query, config_id=PERF_KB_CONFIG_ID, use_compression=use_compression
            )
        except Exception as e:
            return f"Performance knowledge base search error: {e}"

    return [
        StructuredTool.from_function(
            coroutine=_rag_search,
            name="rag_search",
            description=(
                "Search the internal knowledge base (RAG) for relevant information, documentation, "
                "code patterns, or any ingested content. Always use this tool before web search "
                "when looking for project-specific or domain-specific knowledge."
            ),
            args_schema=RagSearchArgs,
            func=lambda **kw: None,
        ),
        StructuredTool.from_function(
            coroutine=_perf_search,
            name="perf_search",
            description=(
                "Search the Performance Analysis Knowledge Base — contains anomaly attribution "
                "playbooks, A/B experiment templates, live stream metric taxonomies, code "
                "performance review checklists, and incident post-mortems. "
                "ALWAYS call this BEFORE web_search for any performance or live-streaming "
                "analysis question. Returns cited, compressed, domain-specific guidance."
            ),
            args_schema=PerfSearchArgs,
            func=lambda **kw: None,
        ),
    ]


