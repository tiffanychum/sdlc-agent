    class GetVersionScoresArgs(BaseModel):
        role: str = Field(description="Agent role (e.g. 'coder').")
        version: str = Field(
            default="latest",
            description="Prompt version to look up (e.g. 'v1', 'v2', 'latest').",
        )
        golden_ids: list = Field(
            default=[],
            description=(
                "Optional list of golden test IDs to filter on. "
                "Leave empty to aggregate all tests for this version."
            ),
        )

    def _get_version_scores(role: str, version: str = "latest", golden_ids: list = []) -> str:
        """
        Return aggregate metric scores + pass rate for a prompt version from regression history.
        Does NOT run new tests — reads stored results. Fast (instant).
        Use to compare before/after versions to decide whether to iterate again.
        """
        try:
            from src.db.database import get_session
            from sqlalchemy import text
            session = get_session()
            try:
                if version == "latest":
                    # Resolve latest version from registry
                    try:
                        from src.prompts.registry import get_registry
                        version = get_registry().latest_version(role)
                    except Exception:
                        version = "v1"

                params: dict = {"role": f"%{role}%", "version": version}
                filter_ids = ""
                if golden_ids:
                    placeholders = ",".join(f":id{i}" for i in range(len(golden_ids)))
                    filter_ids = f"AND golden_case_id IN ({placeholders})"
                    for i, gid in enumerate(golden_ids):
                        params[f"id{i}"] = gid

                rows = session.execute(text(f"""
                    SELECT golden_case_id, overall_pass, quality_scores, deepeval_scores,
                           actual_latency_ms, actual_cost
                    FROM regression_results
                    WHERE actual_agent LIKE :role
                      AND prompt_version = :version
                      {filter_ids}
                    ORDER BY created_at DESC
                """), params).fetchall()
            finally:
                session.close()

            if not rows:
                return (
                    f"No regression data found for role={role} version={version}. "
                    "Run run_regression_subset first to generate data."
                )

            import json as _json
            # Aggregate scores across all rows
            metric_sums: dict[str, list] = {}
            pass_count = 0
            total = len(rows)
            total_cost = 0.0
            total_latency = 0.0
            per_test: dict[str, dict] = {}

            for r in rows:
                gid = r[0]
                if gid in per_test:
                    continue  # take most recent per test
                per_test[gid] = {"pass": bool(r[1])}
                if r[1]:
                    pass_count += 1
                total_cost += r[5] or 0.0
                total_latency += r[4] or 0.0

                for scores_json in [r[2], r[3]]:
                    try:
                        scores = _json.loads(scores_json) if isinstance(scores_json, str) else (scores_json or {})
                        for m, v in scores.items():
                            if isinstance(v, (int, float)):
                                metric_sums.setdefault(m, []).append(v)
                    except Exception:
                        pass

            unique_tests = len(per_test)
            pass_rate = pass_count / unique_tests if unique_tests else 0.0
            avg_cost = total_cost / unique_tests if unique_tests else 0.0
            avg_latency = total_latency / unique_tests if unique_tests else 0.0

            lines = [
                f"Scores for role={role} version={version} "
                f"({unique_tests} unique tests in DB):\n",
                f"  Pass rate:   {pass_rate*100:.1f}%  ({pass_count}/{unique_tests})",
                f"  Avg cost:    ${avg_cost:.5f}",
                f"  Avg latency: {avg_latency/1000:.1f}s\n",
                "  Metric averages:",
            ]
            for m, vals in sorted(metric_sums.items()):
                avg = sum(vals) / len(vals)
                lines.append(f"    {m}: {avg:.3f}  (n={len(vals)})")

            lines.append("\n  Per-test pass/fail:")
            for gid, d in sorted(per_test.items()):
                status = "✓" if d["pass"] else "✗"
                lines.append(f"    {status} {gid}")

            return "\n".join(lines)
        except Exception as e:
            return f"Error fetching version scores: {e}"

