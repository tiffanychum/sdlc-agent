# ── Analytics tools (read-only SQL + regression insights) ─────────────────────

def get_analytics_tools() -> list[StructuredTool]:
    """
    Return analytics tools for the Data Analysis Agent.

    Includes:
      - run_sql: executes read-only SELECT queries against the agent DB
      - get_regression_insights: wrapper around TraceAnalyzer.regression_metric_insights
    """

    # ── Tool 1: run_sql ────────────────────────────────────────────────────────

    class RunSqlArgs(BaseModel):
        sql: str = Field(
            description=(
                "A read-only SELECT SQL query to run against the agent metrics database. "
                "Tables available: regression_results, eval_runs, traces, spans, "
                "golden_test_cases, agents, teams. "
                "Only SELECT statements are allowed — no INSERT, UPDATE, DELETE, or DDL."
            )
        )
        limit: int = Field(
            default=50,
            description="Maximum number of rows to return (default 50, max 200).",
        )

    def _run_sql(sql: str, limit: int = 50) -> str:
        """
        Execute a read-only SQL SELECT query against the agent metrics database.
        Returns results as a formatted table with column headers.

        Safety: only SELECT statements are permitted. Any attempt to run
        INSERT, UPDATE, DELETE, DROP, CREATE, or ALTER will be rejected.
        """
        import re
        # Safety: only allow SELECT
        stripped = sql.strip().upper()
        if not stripped.startswith("SELECT") and not stripped.startswith("WITH"):
            return "ERROR: Only SELECT (and CTEs starting with WITH ... SELECT) are allowed."
        # Block dangerous keywords
        _BLOCKED = re.compile(
            r'\b(INSERT|UPDATE|DELETE|DROP|CREATE|ALTER|TRUNCATE|REPLACE|EXEC|EXECUTE)\b',
            re.IGNORECASE,
        )
        if _BLOCKED.search(sql):
            return "ERROR: Query contains a prohibited keyword (INSERT/UPDATE/DELETE/DROP/etc.)."

        try:
            from src.db.database import get_session
            session = get_session()
            try:
                from sqlalchemy import text
                rows = session.execute(
                    text(sql + (" LIMIT :lim" if "limit" not in sql.lower() else "")),
                    {"lim": min(int(limit), 200)},
                ).fetchall()
                if not rows:
                    return "Query returned 0 rows."

                # Format as markdown table
                cols = list(rows[0]._mapping.keys()) if hasattr(rows[0], "_mapping") else [f"col{i}" for i in range(len(rows[0]))]
                header = " | ".join(str(c) for c in cols)
                sep = " | ".join("---" for _ in cols)
                lines = [f"| {header} |", f"| {sep} |"]
                for row in rows:
                    values = [str(v)[:80] if v is not None else "NULL" for v in row]
                    lines.append("| " + " | ".join(values) + " |")
                return f"Results ({len(rows)} row{'s' if len(rows) != 1 else ''}):\n\n" + "\n".join(lines)
            finally:
                session.close()
        except Exception as e:
            return f"SQL error: {e}"

    # ── Tool 2: get_regression_insights ───────────────────────────────────────

    class RegressionInsightsArgs(BaseModel):
        days: int = Field(
            default=30,
            description="Number of days of regression history to analyse (default 30, max 365).",
        )

    def _get_regression_insights(days: int = 30) -> str:
        """
        Return a structured plain-text summary of regression test performance insights:
        worst-scoring evaluation metrics, most expensive / slowest golden tests,
        agent roles appearing most in failed delegation patterns, and pass rates.
        """
        try:
            from src.tracing.analyzer import TraceAnalyzer
            data = TraceAnalyzer().regression_metric_insights(days=min(int(days), 365))
            summary = data.get("summary", {})
            if summary.get("total_runs", 0) == 0:
                return "No regression runs found in the last {} days. Run some golden tests first.".format(days)

            lines = [
                f"## Regression Insights (last {days} days)\n",
                f"**Total runs**: {summary['total_runs']}  |  **Pass rate**: {(summary.get('pass_rate',0)*100):.1f}%",
                f"**Worst metric**: {summary.get('worst_metric', '—')}  |  **Most failed agent**: {summary.get('most_failed_agent', '—')}",
                "",
                "### Eval Metric Averages (lowest first)",
            ]
            for m in (data.get("worst_metrics") or [])[:8]:
                flag = "❌" if m.get("below_threshold", 0) > 0 else "✓"
                lines.append(
                    f"  {flag} **{m['metric']}**: avg={m['avg']}  threshold={m['threshold']}  "
                    f"below-threshold: {m.get('below_threshold', 0)}/{m.get('count', 0)}"
                )
            lines += ["", "### Most Expensive Tests"]
            for t in (data.get("costliest_tests") or [])[:5]:
                lines.append(f"  - {t['golden_case_id']}: avg=${t['avg_cost']:.5f}  avg_latency={t['avg_latency_ms']:.0f}ms  runs={t['runs']}")
            lines += ["", "### Failed Delegation Patterns (agent role → count)"]
            for agent, cnt in list((data.get("failed_agent_patterns") or {}).items())[:6]:
                lines.append(f"  - {agent}: {cnt} failed runs")
            return "\n".join(lines)
        except Exception as e:
            return f"Error fetching regression insights: {e}"

    return [
        StructuredTool.from_function(
            func=_run_sql,
            name="run_sql",
            description=(
                "Execute a read-only SELECT SQL query against the agent metrics database. "
                "Tables: regression_results, eval_runs, traces, spans, golden_test_cases, "
                "agents, teams. Always show the SQL query you ran in your response. "
                "Only SELECT statements are permitted."
            ),
            args_schema=RunSqlArgs,
        ),
        StructuredTool.from_function(
            func=_get_regression_insights,
            name="get_regression_insights",
            description=(
                "Get a structured summary of regression test performance: worst-scoring "
                "evaluation metrics (faithfulness, answer_relevancy, G-Eval scores), "
                "most expensive and slowest golden tests, agent roles in failed runs, "
                "and pass rates per test. Use this as a starting point before running "
                "custom SQL queries for deeper exploration."
            ),
            args_schema=RegressionInsightsArgs,
        ),
    ]


# ── Prompt Optimization tools (PromptOptimizer Agent) ─────────────────────────

def get_prompt_optimization_tools() -> list[StructuredTool]:
    """
    Tools for the PromptOptimizer meta-agent.

    Provides a complete self-improvement loop:
      1. get_regression_failures   — identify what's failing
      2. retrieve_similar_failures — get few-shot memory from past failures
      3. get_current_prompt        — read the prompt to improve
      4. register_prompt_version   — write improved version to registry
      5. diff_prompts              — show what changed
      6. run_regression_subset     — validate improvement on target tests
    """

    # ── Tool 1: get_regression_failures ───────────────────────────────────────

    class RegressionFailuresArgs(BaseModel):
        role: str = Field(description="Agent role to filter on (e.g. 'coder', 'planner').")
        metric: str = Field(
            default="step_efficiency",
            description="Metric to filter by (e.g. 'step_efficiency', 'tool_usage', 'correctness').",
        )
        threshold: float = Field(
            default=0.7,
            description="Score threshold — runs below this are returned as failures.",
        )
        version: str = Field(
            default="v1",
            description="Prompt version to analyse (e.g. 'v1', 'v2').",
        )
        run_id: Optional[str] = Field(
            default=None,
            description=(
                "Optional regression run_id to pin the baseline. If set, only "
                "failures from that specific run are returned (deterministic "
                "baseline). If omitted, scans all historical runs for the role."
            ),
        )
        limit: int = Field(default=20, description="Max results to return.")

    def _get_regression_failures(
        role: str,
        metric: str = "step_efficiency",
        threshold: float = 0.7,
        version: str = "v1",
        run_id: Optional[str] = None,
        limit: int = 20,
    ) -> str:
        """
        Return failing regression runs for an agent role filtered by a metric threshold.
        Shows golden test ID, metric score, tool calls used, and failure patterns.
        Use this to understand WHAT is failing and WHY before generating a prompt fix.
        """
        try:
            from src.db.database import get_session
            from sqlalchemy import text
            session = get_session()
            try:
                sql = """SELECT golden_case_id, golden_case_name, actual_agent,
                                quality_scores, deepeval_scores, actual_tool_calls,
                                actual_latency_ms, overall_pass, created_at
                         FROM regression_results
                         WHERE actual_agent LIKE :role
                           AND prompt_version = :version
                           AND overall_pass = 0"""
                params: dict = {"role": f"%{role}%", "version": version, "lim": limit}
                if run_id:
                    sql += " AND run_id = :run_id"
                    params["run_id"] = run_id
                sql += " ORDER BY created_at DESC LIMIT :lim"
                rows = session.execute(text(sql), params).fetchall()
            finally:
                session.close()

            if not rows:
                return (
                    f"No failing runs found for role={role} version={version}. "
                    "Either all tests passed or no data exists for this version yet."
                )

            import json as _json
            lines = [
                f"Failing regression runs — role={role} version={version} "
                f"metric={metric} threshold={threshold} ({len(rows)} found):\n"
            ]
            for r in rows:
                try:
                    qs = _json.loads(r[3]) if isinstance(r[3], str) else (r[3] or {})
                    ds = _json.loads(r[4]) if isinstance(r[4], str) else (r[4] or {})
                except Exception:
                    qs, ds = {}, {}

                score = qs.get(metric) or ds.get(metric)
                score_str = f"{score:.3f}" if score is not None else "N/A"
                lines.append(
                    f"  [{r[0]}] {r[1] or ''} | {metric}={score_str} "
                    f"| tool_calls={r[5]} | latency={int(r[6] or 0)}ms"
                )
                # Show all sub-threshold metrics
                all_scores = {**qs, **ds}
                bad = [f"{k}={v:.2f}" for k, v in all_scores.items()
                       if v is not None and isinstance(v, (int, float)) and v < threshold]
                if bad:
                    lines.append(f"    low metrics: {', '.join(bad)}")

            return "\n".join(lines)
        except Exception as e:
            return f"Error fetching regression failures: {e}"

